package br.com.muranodesign.model.report;

public class ReportStatusPlanoEstudos {
	
	 int idPlanejamentoRoteiro;
	 String objetivo;
	 String status;
	 String planoEstudo;
	 String idAluno;
	 String grupo; 
	 String tutoria;
	public int getIdPlanejamentoRoteiro() {
		return idPlanejamentoRoteiro;
	}
	public void setIdPlanejamentoRoteiro(int idPlanejamentoRoteiro) {
		this.idPlanejamentoRoteiro = idPlanejamentoRoteiro;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	public String getPlanoEstudo() {
		return planoEstudo;
	}
	public void setPlanoEstudo(String planoEstudo) {
		this.planoEstudo = planoEstudo;
	}
	public String getIdAluno() {
		return idAluno;
	}
	public void setIdAluno(String idAluno) {
		this.idAluno = idAluno;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getTutoria() {
		return tutoria;
	}
	public void setTutoria(String tutoria) {
		this.tutoria = tutoria;
	}
	
	 
	 

}
